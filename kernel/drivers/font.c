void font_init(void) {}
void font_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg)
{
    if ((uint8_t)c < 32 || (uint8_t)c > 127) c = '?';
    const uint8_t *glyph = g_font[(uint8_t)c - 32];
    for (uint32_t row = 0; row < FONT_H; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < FONT_W; col++) {
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            fb_put_pixel(x + col, y + row, color);
        }
    }
}
void font_draw_str(uint32_t x, uint32_t y, const char *s, uint32_t fg, uint32_t bg)
{
    while (*s) { font_draw_char(x, y, *s++, fg, bg); x += FONT_W; }
}
