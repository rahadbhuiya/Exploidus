/*
 * gui_font_char — draw one character into a pixel buffer.
 *   buf   : ARGB32 pixel buffer
 *   buf_w : width of buffer in pixels (stride)
 *   x, y  : top-left pixel of character cell
 *   c     : ASCII character (32-127)
 *   fg    : foreground ARGB32 color (e.g. 0xFFE6EDF3)
 *   bg    : background ARGB32 color (e.g. 0xFF0D1117), use 0 for transparent
 */
static inline void gui_font_char(uint32_t *buf, int buf_w, int buf_h,
                                  int x, int y,
                                  unsigned char c,
                                  uint32_t fg, uint32_t bg)
{
    if (c < 32 || c > 127) c = '?';
    const uint8_t *glyph = g_gui_font[c - 32];
    for (int row = 0; row < GUI_FONT_H; row++) {
        int py = y + row;
        if (py < 0 || py >= buf_h) continue;
        uint8_t bits = glyph[row];
        for (int col = 0; col < GUI_FONT_W; col++) {
            int px = x + col;
            if (px < 0 || px >= buf_w) continue;
            uint32_t color = (bits & (0x80 >> col)) ? fg : bg;
            if (bg == 0 && !(bits & (0x80 >> col))) continue; /* transparent bg */
            buf[py * buf_w + px] = color;
        }
    }
}

/*
 * gui_font_str — draw a null-terminated string into a pixel buffer.
 */
static inline void gui_font_str(uint32_t *buf, int buf_w, int buf_h,
                                 int x, int y,
                                 const char *s,
                                 uint32_t fg, uint32_t bg)
{
    while (*s) {
        gui_font_char(buf, buf_w, buf_h, x, y, (unsigned char)*s, fg, bg);
        x += GUI_FONT_W;
        s++;
    }
}